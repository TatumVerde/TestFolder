// Об'єкт Math

let x = 100

// Математичні операці

// Math.abs(x) - Повертає модуль числа(абсолютне значення)

let abs = Math.abs(x)
console.log(abs) // 100

// Math.sqrt(x) - Повертає квадратний корінь

let sqrt = Math.sqrt(x)
console.log(sqrt) // 10

// Math.cbrt(x) - Повертає кубічний корінь числа

let cbrt = Math.cbrt(x)
console.log(cbrt) // 4.641...

// Math.ceil(x) - Повертає значення числа, округлене до більшого цілого
x = 10.23

let ceil = Math.ceil(x)
console.log(ceil) // 11

// Math.floor(x) - Повертає значення числа, округлене до меншого цілого

let floor = Math.floor(x)
console.log(floor) // 10

// Math.round(x) - Повертає значення числа, округленного до ближащого цілого

let round = Math.round(x)
console.log(round) // 10

// Math.sin(x) - Повертає сінус числа

let sin = Math.sin(x)
console.log(sin) // -0.7209...

// Math.cos(x) - Повертає косинус числа

let cos = Math.cos(x)
console.log(cos) // -0.6929...


// Математичні константи

// Math.PI - Число пі

let pi = Math.PI
console.log(pi) // 3.14159...

// Math.E - Число Ейлера

let e = Math.E
console.log(e) // 2.718...

