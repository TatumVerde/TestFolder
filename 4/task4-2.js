/* If 2
Дано целое число. Если оно является положительным, то прибавить к
нему 1; в противном случае вычесть из него 2. Вывести полученное число. */

// користувач вводить число
let x = parseInt(prompt('Введіть число'))

// Якщо число додатнє, добавляємо до нього 1, в любому іншому випадку віднімаємо 2
if (x > 0) {
    x = x + 1
} else {
    x = x - 2
}

// Виводимо число, яке получилось
alert(x)